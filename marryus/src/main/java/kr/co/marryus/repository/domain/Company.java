package kr.co.marryus.repository.domain;

public class Company {
	int comNo;
	String comName;
	String comAddr;
	String comAddrDetail;
	String comPhone;
	String comType;
	String comOkay;
	public String getComOkay() {
		return comOkay;
	}
	public void setComOkay(String comOkay) {
		this.comOkay = comOkay;
	}
	public int getComNo() {
		return comNo;
	}
	public void setComNo(int comNo) {
		this.comNo = comNo;
	}
	public String getComName() {
		return comName;
	}
	public void setComName(String comName) {
		this.comName = comName;
	}
	public String getComAddr() {
		return comAddr;
	}
	public void setComAddr(String comAddr) {
		this.comAddr = comAddr;
	}
	public String getComAddrDetail() {
		return comAddrDetail;
	}
	public void setComAddrDetail(String comAddrDetail) {
		this.comAddrDetail = comAddrDetail;
	}
	public String getComPhone() {
		return comPhone;
	}
	public void setComPhone(String comPhone) {
		this.comPhone = comPhone;
	}
	public String getComType() {
		return comType;
	}
	public void setComType(String comType) {
		this.comType = comType;
	}
	

}
